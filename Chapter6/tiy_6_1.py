# 6-1: Storing info about a person
demo = {
	'first_name': 'Jenny'
	,'last_name': 'FromTheBlock'
	,'age': 45
	,'city': 'Da Bronx'
	}

print("Don't be fooled by the rocks that she got, she's still " + 
	demo['first_name'] + " " + demo['last_name'] + "!")

print("\n" + demo['first_name'] +
	" is " + str(demo['age']) + " years old, hailing from " + 
	demo['city'] + ".")

